package com.cg.demo;

public class Operator {
 public static void main(String[] args) {
	
	 int i = 10, j = 3, k;

		k = i + j;
		System.out.println(k);

		k = i - j;
		System.out.println(k);

		k = i * j;
		System.out.println(k);

		k = i / j;
		System.out.println(k);

		k = i % j;
		System.out.println(k);
}
}
    